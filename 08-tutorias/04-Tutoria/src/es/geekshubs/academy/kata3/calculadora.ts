export const suma = (a:number, b:number) : number => {
    return a + b 
}

export const resta = (a:number, b:number) : number => {
    return a - b 
}

export const division = (a:number, b:number) : number => {
    //if( b === 0)
    //  throw new Error("Division: Denominador 0 !!!!")
    return a / b 
}